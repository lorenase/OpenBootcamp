package IntroduccionProgramacion.Ejercicios.Ejercicios4.Ej4;

public class DoWhile {
    public static void main(String[] args){
        int numeroDoWhile = 3;
        do{
            System.out.println(numeroDoWhile);
            numeroDoWhile = numeroDoWhile + 1;
        } while (numeroDoWhile < 3);

    }
}
