package IntroduccionProgramacion.Ejercicios.Ejercicios4.Ej4;

public class If {
    public static void main(String[] args) {
        int numeroIf = 0;
        if (numeroIf > 0) {
            System.out.println("Es positivo");
        } else if (numeroIf < 0) {
            System.out.println("Es negativo");
        } else {
            System.out.println("Es 0");
        }
    }
}